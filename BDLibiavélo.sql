CREATE DATABASE IF NOT EXISTS velodb;
USE velodb;

-- Table Brand
CREATE TABLE brand (
    name VARCHAR(50) NOT NULL,
    waranty_duration INT NOT NULL,
    PRIMARY KEY (name)
);

-- Table Localite
CREATE TABLE localite (
    postal_code INT NOT NULL,
    name VARCHAR(50) NOT NULL,
    PRIMARY KEY (postal_code, name)
);

-- Table Station
CREATE TABLE station (
    station_number INT NOT NULL,
    name VARCHAR(50) NOT NULL,
    street VARCHAR(50) NOT NULL,
    street_number INT NOT NULL,
    postal_code INT NOT NULL,
    local_name VARCHAR(50) NOT NULL,
    PRIMARY KEY (station_number),
    FOREIGN KEY (postal_code, local_name) REFERENCES localite(postal_code, name)
);

-- Table Bike
CREATE TABLE bike (
    serial_number INT NOT NULL,
    is_electric BOOLEAN NOT NULL,
    buying_date DATE NOT NULL,
    battery_level FLOAT,
    nb_kilometer INT,
    station_id INT NOT NULL,
    brand_name VARCHAR(50) NOT NULL,
    PRIMARY KEY (serial_number),
    FOREIGN KEY (brand_name) REFERENCES brand(name),
    FOREIGN KEY (station_id) REFERENCES station(station_number)
);

-- Table Subscription
CREATE TABLE subscription (
    card_number INT NOT NULL,
    price FLOAT NOT NULL,
    date DATE NOT NULL,
    caution_paid BOOLEAN NOT NULL,
    subscription_paid BOOLEAN NOT NULL,
    PRIMARY KEY (card_number)
);

-- Table Member
CREATE TABLE member (
    national_number VARCHAR(50) NOT NULL,
    first_name VARCHAR(50) NOT NULL,
    last_name VARCHAR(50) NOT NULL,
    email VARCHAR(50) NOT NULL,
    phone_number INT NOT NULL,
    got_discount BOOLEAN NOT NULL,
    street VARCHAR(50) NOT NULL,
    street_number INT NOT NULL,
    sub_id INT NOT NULL,
    postal_code INT NOT NULL,
    name VARCHAR(50) NOT NULL,
    PRIMARY KEY (national_number),
    FOREIGN KEY (sub_id) REFERENCES subscription(card_number),
    FOREIGN KEY (postal_code, name) REFERENCES localite(postal_code, name)
);

-- Table Rental
CREATE TABLE rental (
    id INT NOT NULL,
    start_date DATE NOT NULL,
    return_date DATE,
    comment TEXT,
    had_issue BOOLEAN NOT NULL,
    bike_id INT NOT NULL,
    sub_id INT NOT NULL,
    PRIMARY KEY (id),
    FOREIGN KEY (bike_id) REFERENCES bike(serial_number),
    FOREIGN KEY (sub_id) REFERENCES subscription(card_number)
);

-- Table RepairStatus
CREATE TABLE repair_status (
    libelle VARCHAR(50) NOT NULL,
    PRIMARY KEY (libelle)
);

-- Table Repair
CREATE TABLE repair (
    id INT NOT NULL AUTO_INCREMENT,
    cost DECIMAL(18, 2) NOT NULL,
    date DATE NOT NULL,
    libelle VARCHAR(50) NOT NULL,
    serial_number INT NOT NULL,
    mechanic_id INT NOT NULL,
    PRIMARY KEY (id),
    FOREIGN KEY (serial_number) REFERENCES bike(serial_number),
    FOREIGN KEY (libelle) REFERENCES repair_status(libelle),
    FOREIGN KEY (mechanic_id) REFERENCES mechanic(badge_id)
);

-- Table Mechanic
CREATE TABLE mechanic (
    badge_id INT NOT NULL,
    first_name VARCHAR(50) NOT NULL,
    last_name VARCHAR(50) NOT NULL,
    email VARCHAR(50) NOT NULL,
    phone_number INT NOT NULL,
    PRIMARY KEY (badge_id)
);

